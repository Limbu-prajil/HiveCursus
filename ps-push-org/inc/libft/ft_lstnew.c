#include "libft.h"

t_list	*ft_lstnew(int value)
{
	t_list	*new_node;

	new_node = (t_list *)malloc(sizeof(t_list));
	if (!new_node)
		return (NULL);
	new_node->prev = new_node;
	new_node->nbr = value;
	new_node->next = new_node;
	return (new_node);
}
