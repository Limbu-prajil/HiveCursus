#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		count;
	t_list	*tmp;

	count = 0;
	tmp = lst;
	while (lst->next != tmp)
	{
		lst = lst->next;
		count++;
	}
	count++;
	return (count);
}
